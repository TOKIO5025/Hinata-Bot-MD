import fetch from "node-fetch";
import yts from "yt-search";

let handler = async (m, { conn, text, isROwner, isPrems }) => {
  const fake = { quoted: m };

  // Verificaci√≥n r.canal
  if (!global.db.data.chats[m.chat].canal && !isROwner && !isPrems) {
    return conn.reply(m.chat, '‚ùå Este comando solo est√° disponible en canales autorizados.\nAct√≠valo con: *.rcanal on*', m);
  }

  if (!text) {
    return conn.reply(m.chat, `‚ö° Por favor, ingresa el nombre de una canci√≥n de Spotify.`, m, fake);
  }

  await m.react('üïí');
  conn.reply(m.chat, `*üéß Buscando tu canci√≥n en Spotify...*`, m, fake);

  try {
    let res = await fetch(`https://api.nekorinn.my.id/downloader/spotifyplay?q=${encodeURIComponent(text)}`);
    let gyh = await res.json();

    if (!gyh.result || !gyh.result.downloadUrl) throw '‚ùå No se encontr√≥ ninguna canci√≥n.';

    const search = await yts(text);
    if (!search.videos || search.videos.length === 0) throw '‚ùå No se encontr√≥ un video relacionado.';

    const videoInfo = search.videos[0];
    const { title, thumbnail, timestamp: duration, views, url } = videoInfo;

    const doc = {
      audio: { url: gyh.result.downloadUrl },
      mimetype: 'audio/mpeg',
      fileName: `${title}.mp3`,
      contextInfo: {
        externalAdReply: {
          showAdAttribution: true,
          mediaType: 2,
          mediaUrl: url,
          title: title,
          body: `Duraci√≥n: ${duration} | Reproducciones: ${views.toLocaleString()}`,
          sourceUrl: url,
          thumbnailUrl: thumbnail || "https://h.uguu.se/gwCZoshl.jpg",
          renderLargerThumbnail: true
        }
      }
    };

    await conn.sendMessage(m.chat, doc, { quoted: m });
    await m.react('‚úÖ');

  } catch (e) {
    console.error(e);
    await m.react('‚ùå');
    conn.reply(m.chat, 'üö´ Hubo un error al buscar la canci√≥n.', m, fake);
  }
};

handler.help = ['spotify *<texto>*'];
handler.tags = ['descargas'];
handler.command = ['spotify'];
handler.register = true;
handler.canal = true; // <-- r.canal activado

export default handler;
